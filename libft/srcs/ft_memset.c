#include <libft.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s;

	i = 0;
	s = b;
	c = (unsigned char)c;
	while (i < len)
	{
		*(s + i) = c;
		i++;
	}
	return (s);
}
