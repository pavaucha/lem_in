#include <libft.h>

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;
	int		same;

	i = 0;
	same = 1;
	if (s1 == NULL || s2 == NULL)
		return (0);
	if (*(s1 + 0) != *(s2 + 0))
		same = 0;
	while (*(s1 + i) == *(s2 + i) && *(s1 + i) != '\0' && *(s2 + i) != '\0')
		i++;
	if (*(s1 + i) != *(s2 + i))
		same = 0;
	return (same);
}
