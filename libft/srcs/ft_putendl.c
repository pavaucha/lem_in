#include <libft.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	endl;

	i = 0;
	endl = '\n';
	if (s == NULL)
		return ;
	while (*(s + i) != '\0')
		i++;
	write(1, s, i);
	write(1, &endl, 1);
}
